/*

******************************************* JavaScript in One file *******************************************

1. JavaScript is more than just a language - it's an entire ecosystem that spans framworks, task runners, server-side development and more

2. Javascript is a dynamically typed language. It means you don't need to worry about datatype when creating variables.
   They are automatically resolved at runtime.
   
3. The difference between first two is that let has a block scope and var doesn't.

4. ECMAScript 6 is ES6

*/

var myVariable1 = 5;
let myVariable2 = 5;
const myVariable3 = 5; // It just creates a constant. Once declared it cannot be changed.

/*
const database = "someDatabaseName";
database = "newDatabaseName";  // This will throw Error
*/

// var
for(var i = 0; i < 5; ++i) {
    console.log(i);
}
console.log(i); // This will print 5

// let 
for(let j = 0; j < 5; ++j) { 
    console.log(j);
}
// console.log(j); // Error. variable declared using `let` cannot be referenced outside loop

// Another difference between them is is that variables declared using var can be redeclared.
// var
var myVariable = 5;
var myVariable = 10; // Fine
// let
let variable = 5;
// let variable = 10; // Error

// var can introduce some bug so better to use let and const
x = 7 // it is a global variable
console.log(x);

function myFunc() {
  x = 11;
  let y = 2;
  var z = 3;
  const w = 4;
}
myFunc();

console.log(x); // Prints 11
// console.log(y); // Error
// console.log(z); // Error
// console.log(w); // Error

console.log(window.x); // Prints 11 

